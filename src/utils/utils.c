#include "../includes/philosophers.h"

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	if (!str)
	{
	    write(1, "(null)", 6);
	}
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	
}